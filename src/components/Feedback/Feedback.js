import ReactDOM from 'react-dom';
import App from '../App/App';
import { Switch, Route } from 'react-router-dom';

ReactDOM.render(
    <Router>
        <App />
    </Router>,
    document.getElementById('root')
);


class Feedback extends Component {
    constructor(props) {
        super(props);
        this.state = {
          step: 1
        };
      }
    


	render() {
		switch (this.state.step) {
			case 1:
				return <FirstPage />
			case 2:
				return <SecondPage />
			case 3:
				return <ThirdPage />
			case 4:
				return <FourthPage />
            case 5:
                return <FifthPage />
		}
	}
}


export default Feedback;